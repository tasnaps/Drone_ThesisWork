import os
import shutil
import sys
from pathlib import Path


def find_histogram_files(directory='.'):
    """
    Recursively find all histogram PNG files generated by the analysis script.
    """
    histogram_files = []

    for root, dirs, files in os.walk(directory):
        for file in files:
            if file.endswith('.png'):
                full_path = os.path.join(root, file)
                histogram_files.append(full_path)

    return histogram_files


def extract_meaningful_path(file_path, base_directory):
    """
    Extract meaningful path components from the file path to create organized naming.

    For example:
    C:\...\CNN_LSTM\1Epoch\cnn_lstm_evaluation_20250905_152242\DroneAudio\results_detailed_files_histogram.png
    becomes: CNN_LSTM__1Epoch__DroneAudio__results_detailed_files_histogram.png
    """
    # Convert to Path objects for easier manipulation
    file_path = Path(file_path)
    base_path = Path(base_directory)

    try:
        # Get relative path from base directory
        relative_path = file_path.relative_to(base_path)

        # Extract path components (excluding the filename)
        path_parts = list(relative_path.parts[:-1])  # All parts except filename
        filename = relative_path.parts[-1]  # Just the filename

        # Filter out timestamp-like directories (contain evaluation_ and dates)
        filtered_parts = []
        for part in path_parts:
            # Skip evaluation directories with timestamps
            if 'evaluation_' in part and any(char.isdigit() for char in part):
                continue
            # Skip model directories with timestamps
            if 'model_' in part and any(char.isdigit() for char in part):
                continue
            filtered_parts.append(part)

        # Create meaningful name by joining path components
        if filtered_parts:
            meaningful_name = '__'.join(filtered_parts) + '__' + filename
        else:
            meaningful_name = filename

        # Clean up the name (remove invalid characters for filenames)
        meaningful_name = meaningful_name.replace(':', '_').replace('/', '_').replace('\\', '_')

        return meaningful_name, filtered_parts

    except ValueError:
        # If file is not under base directory, just use the filename
        return file_path.name, []


def organize_graphs(source_directory='.', output_directory='organized_graphs'):
    """
    Organize all histogram graphs into a structured output directory.
    """
    if not os.path.exists(source_directory):
        print(f"Error: Source directory '{source_directory}' does not exist")
        return

    # Find all histogram files
    histogram_files = find_histogram_files(source_directory)

    if not histogram_files:
        print(f"No histogram files (*_histogram.png) found in directory: {source_directory}")
        return

    print(f"Found {len(histogram_files)} histogram file(s) in directory tree: {source_directory}")

    # Create output directory
    output_path = os.path.join(source_directory, output_directory)
    os.makedirs(output_path, exist_ok=True)
    print(f"Created output directory: {output_path}")

    # Track statistics
    copied_count = 0
    error_count = 0
    models_found = set()
    datasets_found = set()

    # Process each histogram file
    for file_path in histogram_files:
        try:
            meaningful_name, path_parts = extract_meaningful_path(file_path, source_directory)

            # Track models and datasets for summary
            if path_parts:
                if len(path_parts) >= 1:
                    models_found.add(path_parts[0])  # e.g., CNN_LSTM
                if len(path_parts) >= 3:
                    datasets_found.add(path_parts[2])  # e.g., DroneAudio

            # Copy file with new name
            destination_path = os.path.join(output_path, meaningful_name)
            shutil.move(file_path, destination_path)

            print(f"  ✓ Copied: {os.path.basename(file_path)} -> {meaningful_name}")
            copied_count += 1

        except Exception as e:
            print(f"  ✗ Error processing {file_path}: {e}")
            error_count += 1

    # Print summary
    print(f"\n=== Organization Summary ===")
    print(f"Total histogram files found: {len(histogram_files)}")
    print(f"Files successfully moved: {copied_count}")
    print(f"Files with errors: {error_count}")
    print(f"Output directory: {output_path}")

    if models_found:
        print(f"Models found: {', '.join(sorted(models_found))}")
    if datasets_found:
        print(f"Datasets found: {', '.join(sorted(datasets_found))}")

    # Create a summary file with the file mapping
    create_file_mapping_summary(histogram_files, output_path, source_directory)


def create_file_mapping_summary(histogram_files, output_path, source_directory):
    """
    Create a text file that shows the mapping between original and new filenames.
    """
    summary_file = os.path.join(output_path, 'file_mapping_summary.txt')

    with open(summary_file, 'w', encoding='utf-8') as f:
        f.write("File Organization Summary\n")
        f.write("=" * 50 + "\n\n")
        f.write(f"Source Directory: {source_directory}\n")
        f.write(f"Output Directory: {output_path}\n")
        f.write(f"Total Files Processed: {len(histogram_files)}\n\n")
        f.write("File Mapping:\n")
        f.write("-" * 30 + "\n")

        for file_path in histogram_files:
            meaningful_name, path_parts = extract_meaningful_path(file_path, source_directory)
            relative_original = os.path.relpath(file_path, source_directory)
            f.write(f"Original: {relative_original}\n")
            f.write(f"New Name: {meaningful_name}\n")
            f.write(f"Path Parts: {' -> '.join(path_parts) if path_parts else 'Root level'}\n")
            f.write("\n")

    print(f"  📄 Created file mapping summary: {summary_file}")


if __name__ == "__main__":
    if len(sys.argv) > 2:
        source_dir = sys.argv[1]
        output_dir = sys.argv[2]
        organize_graphs(source_dir, output_dir)
    elif len(sys.argv) > 1:
        source_dir = sys.argv[1]
        organize_graphs(source_dir)
    else:
        organize_graphs()
